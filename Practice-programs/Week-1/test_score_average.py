# Get the test scores and assign them to the
# test1, test2, and test3 variables

test1 = float(input('Enter the first score: '))
test2 = float(input('Enter the second score: '))
test3 = float(input('Enter the third score: '))

# Calculate average of the three scores
# And assign the results to the average variable.

average = (test1 + test2 + test3) / 3.0

# Display average

print('Here is your average test score', average)
